using MonoDragons.Core.Common;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MegaBuy.Calls
{
    public static class CollectionExtensionsMethods
    {
        public static KeyValuePair<T, T2> Random<T, T2>(this Dictionary<T, T2> dictionary)
        {
            return dictionary.ElementAt(Rng.Int(dictionary.Count));
        }

        public static T Random<T>(this T[] array)
        {
            return array[Rng.Int(array.Length)];
        }

        public static T Random<T>(this List<T> list)
        {
            return list[Rng.Int(list.Count)];
        }
    }
}
